import 'dart:io' as io;

import 'package:dart_mcp/server.dart' show ProtocolVersion;
import 'package:dart_mcp/stdio.dart';
import 'package:path/path.dart' as p;
import 'package:promptforge/mcp/promptforge_mcp_server.dart';

/// App version, stamped at build time by the release CI
/// (`dart compile exe --define=PROMPTFORGE_VERSION=<v>`); defaults for dev runs.
const String kAppVersion =
    String.fromEnvironment('PROMPTFORGE_VERSION', defaultValue: '1.0.0');

const String _usage = '''
PromptForge MCP server (read-only, stdio).

Usage: promptforge_mcp [options]

Options:
  --db <path>     Path to the PromptForge SQLite database. Defaults to the
                  app's data location for this OS. PromptForge → Settings →
                  MCP Server generates a ready-to-paste command with the
                  correct --db for your machine.
  --version, -v   Print version + supported MCP protocol range and exit.
  --help, -h      Show this help and exit.

This server is OFF BY DEFAULT. Enable it in PromptForge → Settings → MCP Server,
then register it with your MCP client using the snippets shown there.
''';

void main(List<String> args) {
  String? dbPath;
  for (var i = 0; i < args.length; i++) {
    final a = args[i];
    if (a == '--version' || a == '-v') {
      const protos = ProtocolVersion.values;
      io.stdout.writeln('PromptForge MCP server $kAppVersion '
          '(MCP protocol ${protos.first.versionString}..${protos.last.versionString})');
      io.exit(0);
    } else if (a == '--help' || a == '-h') {
      io.stdout.writeln(_usage);
      io.exit(0);
    } else if (a == '--db') {
      if (i + 1 >= args.length) {
        io.stderr.writeln('--db requires a path argument.');
        io.exit(2);
      }
      dbPath = args[++i];
    } else if (a.startsWith('--db=')) {
      dbPath = a.substring('--db='.length);
    } else {
      io.stderr.writeln('Unknown argument: $a\n\n$_usage');
      io.exit(2);
    }
  }

  // Connect the server to stdio; it stays alive serving the MCP client. All
  // failure modes (disabled, missing/busy DB, schema mismatch) are reported as
  // clean errors on `initialize`, never as a crash.
  PromptForgeMcpServer(
    stdioChannel(input: io.stdin, output: io.stdout),
    dbPath: dbPath ?? _defaultDbPath(),
    appVersion: kAppVersion,
  );
}

/// Best-effort default DB path mirroring the app's
/// `getApplicationDocumentsDirectory()` per OS. Real client configs generated by
/// Settings pass an explicit `--db`, so this is only the no-argument fallback.
String _defaultDbPath() {
  final env = io.Platform.environment;
  final home = env['HOME'] ?? env['USERPROFILE'] ?? '.';
  final String docs;
  if (io.Platform.isMacOS) {
    // The app is sandboxed; its Documents dir lives inside the container.
    docs = p.join(home, 'Library', 'Containers',
        'io.github.levietanhhuy.promptforge', 'Data', 'Documents');
  } else {
    // Linux + Windows: getApplicationDocumentsDirectory() → ~/Documents.
    docs = p.join(home, 'Documents');
  }
  return p.join(docs, 'promptforge_db.sqlite');
}
